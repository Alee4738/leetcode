const modulus = Math.pow(10, 9) + 7;

function nChooseK(n: number, k: number): bigint {
  let result = BigInt(1);
  for (let i = n; i > n - k; i--) {
    result *= BigInt(i);
  }
  for (let i = 2; i <= k; i++) {
    result /= BigInt(i);
  }
  return result;
}

function threeSumMulti(arr: number[], target: number): number {
  const index = new Map<number, number[]>();
  for (let i = 0; i < arr.length; i++) {
    const num = arr[i];
    const indicesWithSameNum = index.get(num) ?? [];
    indicesWithSameNum.push(i);
    index.set(num, indicesWithSameNum);
  }

  let result = BigInt(0);
  const uniqueNums = Array.from(index.keys());
  uniqueNums.sort((a, b) => a - b);
  // Make every twoSum possible
  for (let i = 0; i < uniqueNums.length; i++) {
    const numI = uniqueNums[i];
    for (let j = i; j < uniqueNums.length; j++) {
      const numJ = uniqueNums[j];
      const sumIJ = numI + numJ;
      const complement = target - sumIJ;
      const indicesWithComplement = index.get(complement);
      // I need to somehow allow 1 2 5 but not allow 1 5 2 and 2 5 1
      // Say numJ must be <= complement, or else it has already been counted
      if (indicesWithComplement !== undefined && numJ <= complement) {
        const indicesWithNumI = index.get(numI) ?? [];
        const indicesWithNumJ = index.get(numJ) ?? [];
        // console.log(target, 'is the sum of', numI, numJ, complement)
        if (numI !== numJ && numI !== complement && numJ !== complement) {
          // console.log(1)
          result += BigInt(
            indicesWithNumI.length *
              indicesWithNumJ.length *
              indicesWithComplement.length
          );
        } else if (numI === numJ && numI !== complement) {
          // console.log(2)
          result +=
            nChooseK(indicesWithNumI.length, 2) *
            BigInt(indicesWithComplement.length);
        } else if (numI === complement && numI !== numJ) {
          // console.log(3)
          result +=
            nChooseK(indicesWithNumI.length, 2) *
            BigInt(indicesWithNumJ.length);
        } else if (numJ === complement && numJ !== numI) {
          // console.log(4)
          result +=
            nChooseK(indicesWithNumJ.length, 2) *
            BigInt(indicesWithNumI.length);
        } else if (numI === numJ && numJ === complement) {
          // console.log(5)
          // console.log('indicesWithNumI.length', indicesWithNumI.length)
          result += BigInt(nChooseK(indicesWithNumI.length, 3));
        } else {
          throw new Error('impossible');
        }
        result %= BigInt(modulus);
      }
    }
  }

  // Slower solution
  // for (let i = 0; i < arr.length - 2; i++) {
  //     const numI = arr[i];
  //     for (let j = i + 1; j < arr.length - 1; j++) {
  //         const numJ = arr[j];
  //         const sumIJ = numI + numJ;
  //         const complement = target - sumIJ;
  //         const indicesWithComplement = index.get(complement);
  //         if (indicesWithComplement !== undefined) {
  //             const indicesGreaterThanJ = indicesWithComplement.filter(k => k > j);
  //             // console.log(`(${numI},${numJ},${complement}) at indices (${i},${j},[${indicesGreaterThanJ}])`);
  //             result += indicesGreaterThanJ.length
  //         }
  //     }
  // }
  return Number(result);
}

